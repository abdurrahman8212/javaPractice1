package Day03_EscapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {


        System.out.println("Java \nPython \nC#");
        System.out.println("---------------------------");

        System.out.println("Hello Cydeo \nHow are you all today ? \nIt's nice to see all! \nWhat class do we have next week?");
        System.out.println("-------------------------------");
        System.out.println("\tJava is cool Programming Language");
        System.out.println("---------------------------------");
        System.out.println("/ \\");
        System.out.println("-----------------------------------");
        System.out.println("My favourite TV show is \"Game of Thrones\""); // In order to print double quote, we need to use escape sequences


    }
}
